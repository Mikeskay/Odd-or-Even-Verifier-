package oddoreven.base;

public class OddOrEven {

    public OddOrEven(){
        
    }

    public static void printOddOrEven(int num){

        if(num % 2 == 0){
            System.out.println("Number entered is an even integer value.");
        }

        else {
            System.out.println("Number entered is an odd integer value.");
        }


    }

    

}
